import math
print("enter the number:")
n = int(input())
square_root = math.sqrt(n)
print("square root of the number is :", square_root)
logarithm_natural = math.log(n)
print("natural logarithm of the number is :", logarithm_natural)
sine_value = math.sin(n)
print("sine of the number (in radians) is :", sine_value)                                   
